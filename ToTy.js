let time, score, word, gameOver, myBestScore;

window.onload = setStart();
function setStart() {
    gameOver = true;
    time = { time: 60, anotherTime: 0, use: "time" };
    score = 0;
    myBestScore = localStorage.getItem("bestScoreToToTy--game.__--code:-457");
    bestScoreBox.innerHTML = `your best score: ${myBestScore}`;
    timeBox.style.textShadow = "2px 2px 0px #fff9, 3px 3px 5px #10fff696";
    timeBox.innerHTML = "1:00";
    wordBox.innerHTML = "let's start!";
}

function start() {
    if (startBox.innerHTML === "start") {
        letsStart();
    } else if (startBox.innerHTML === "restart") {
        restart();
    }
}

function letsStart() {
    startBox.innerHTML = "3";
    setTimeout(() => {
        startBox.innerHTML = "2";
    }, 1000);
    setTimeout(() => {
        startBox.innerHTML = "1";
    }, 2000);
    setTimeout(() => {
        startBox.innerHTML = "Go!";
        Go();
    }, 3000);
}

function Go() {
    input.value = "";
    scoreBox.innerHTML = score;
    gameOver = false;
    setTimeout(() => {
        startBox.innerHTML = "restart";
    }, 1000);
    backgroundAudio.volume = 0.25;
    backgroundAudio.play();
    timeSystem();
    choiceWord();
}

function restart() {
    backgroundAudio.pause();
    backgroundAudio.currentTime = 0;
    ticTacAudio.pause();
    ticTacAudio.currentTime = 0;
    endGame();
}

function timeSystem() {
    if (!gameOver) {
        if (time.use === "time") {
            time.time--;
            if (time.time >= 10) {
                timeBox.innerHTML = `0:${time.time}`;
            } else if (time.time >= 0) {
                timeBox.style.color = "red";
                timeBox.style.textShadow =
                    "2px 2px 0px #ff101096, 3px 3px 5px #10fff696";
                timeBox.innerHTML = `0:0${time.time}`;
                ticTacAudio.play();
            } else if (time.time < 0) {
                endGame();
            }
            if (time.time === 0) {
                timeBox.style.color = "green";
                timeBox.style.textShadow =
                    "2px 2px 0px #fff, 15px 15px 50px #10ffa4, -15px 15px 50px #10ffa4, 15px -15px 50px #10ffa4, -15px -15px 50px #10ffa4";
                time.use = "anotherTime";
            }
        } else {
            backgroundBonusAudio.play();
            time.anotherTime--;
            if (time.anotherTime >= 10) {
                timeBox.innerHTML = `0:${time.anotherTime}`;
            } else if (time.anotherTime >= 0) {
                timeBox.innerHTML = `0:0${time.anotherTime}`;
            } else if (time.anotherTime < 0) {
                endGame();
            }
        }
        setTimeout(timeSystem, 1000);
    }
}

function choiceWord() {
    let index = Math.floor(Math.random() * allWords.length);
    word = allWords[index];
    wordBox.innerHTML = word;
}

window.addEventListener("keydown", () => {
    setTimeout(() => {
        if (!gameOver && input.value === word) {
            score++;
            if (time.use === "time") {
                time.anotherTime++;
            }
            correctAudio.play();
            input.value = "";
            scoreBox.innerHTML = score;
            choiceWord();
        }
    });
});

function endGame() {
    timeBox.style.color = "white";
    dingDongAudio.play();
    startBox.innerHTML = "start";
    backgroundBonusAudio.pause();
    backgroundBonusAudio.currentTime = 0;
    bestScore();
    setStart();
}

