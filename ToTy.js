let time, score, word, gameOver;

window.onload = setStart();
function setStart() {
    gameOver = true;
    time = 60;
    score = 0;
    timeBox.innerHTML = "1:00";
    wordBox.innerHTML = "lets start!";
}

function start() {
    if (gameOver) {
        scoreBox.innerHTML = score;
        gameOver = false;
        startBox.innerHTML = "restart"
        backgroundAudio.volume = 0.25;
        backgroundAudio.play();
        timeSystem();
        choiceWord();
    } else {
        restart();
    }
}

function restart() {
    backgroundAudio.pause();
    endGame()
}

function timeSystem() {
    if (!gameOver) {
        time--;
        if (time >= 10) {
            timeBox.innerHTML = `0:${time}`;
        } else if (time >= 0) {
            timeBox.style.color = "red";
            timeBox.innerHTML = `0:0${time}`;
            ticTacAudio.play();
        } else if (time < 0) {
            endGame();
        }
        setTimeout(timeSystem, 1000);
    }
}

function choiceWord() {
    let index = Math.floor(Math.random() * allWords.length);
    word = allWords[index];
    wordBox.innerHTML = word;
}

window.addEventListener("keydown", () => {
    setTimeout(() => {
        if (!gameOver && input.value === word) {
            score++;
            correctAudio.play();
            input.value = "";
            scoreBox.innerHTML = score;
            choiceWord();
        }
    }, 1)
});

function endGame() {
    timeBox.style.color = "white";
    dingDongAudio.play();
    startBox.innerHTML = "start";
    setStart();
}
